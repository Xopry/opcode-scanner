module.exports = pak => {
	if (!pak.parse()) return false;

	return (
		[1, 12].includes(pak.parsed.invType) &&
		[0, 1, 2, 3, 4, 5, 6, 7].includes(pak.parsed.bankPos / 72)
	);
};

